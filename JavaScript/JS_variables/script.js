// Single Line Comment


// JavaScript Variable:
// var - This is globally scoped and can be update and re-declared 
// let - This is block scoped and can be update but cannot re-declare & 
// const - This is block scoped and cannot be update or re-declare


// Global Scope: A variable declared outside a function, this means all the scripts and functions on a web application or website can be access this variable.
// Block Scope: It will be declared inside a block. This variable will be inside functions, loops, if-statements or other declarations.

var name = "Sanjay";
console.log(name);

let lang = "NodeJS";
console.log(lang);

const num = 5;
console.log(num);



name = "Alen";
console.log(name);

lang = "Python";
console.log(lang);

num = 10;
console.log(num)